/* 8x8 폰트로 문자 1개 그리기 (scale 배율 지원) */
void draw_char_scaled(unsigned short *screen, int x, int y, unsigned char c, int color, int scale) {
    int row, col, sx, sy;
    if (scale < 1) scale = 1;

    for (row = 0; row < 8; row++) {
        unsigned char bits = font_data[c][row];
        for (col = 0; col < 8; col++) {
            if (bits & (1 << (7 - col))) {
                /* scale 만큼 블록으로 찍기 */
                for (sy = 0; sy < scale; sy++) {
                    for (sx = 0; sx < scale; sx++) {
                        setpixel(screen, x + col * scale + sx, y + row * scale + sy, color);
                    }
                }
            }
        }
    }
}

/* 8x8 폰트로 문자열 그리기 (scale 배율 지원) */
void draw_text_scaled(unsigned short *screen, int x, int y, const char *text, int color, int scale) {
    int i;
    int cx = x;
    int spacing = scale; /* 글자 사이 간격 */

    if (scale < 1) scale = 1;

    for (i = 0; text[i] != '\0'; i++) {
        unsigned char c = (unsigned char)text[i];
        draw_char_scaled(screen, cx, y, c, color, scale);
        cx += (8 * scale) + spacing;
    }
}

/* 기존 인터페이스 유지: 작은 텍스트(기본 scale=1) */
void draw_text(unsigned short *screen, int x, int y, const char *text, int color) {
    draw_text_scaled(screen, x, y, text, color, 1);
}

/* 문자열을 화면 가운데 정렬해서 그리기 */
void draw_text_centered(unsigned short *screen, int y, const char *text, int color, int scale) {
    int len = 0;
    int spacing, width, x;

    while (text[len] != '\0') len++;

    if (scale < 1) scale = 1;
    spacing = scale;
    width = len * (8 * scale + spacing);
    if (len > 0) width -= spacing;

    x = (SCREEN_WIDTH - width) / 2;
    if (x < 0) x = 0;
    draw_text_scaled(screen, x, y, text, color, scale);
}

