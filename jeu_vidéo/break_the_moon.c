/* 픽셀 그리기 */
void setpixel(unsigned short *screen, int x, int y, int color) {
    unsigned short *ptr;
    int offset, bit;
    int plane;
    
    if (x < 0 || x >= SCREEN_WIDTH || y < 0 || y >= SCREEN_HEIGHT)
        return;
    
    offset = y * 160 + (x >> 4) * 8;
    bit = 15 - (x & 15);
    
    for (plane = 0; plane < 4; plane++) {
        ptr = (unsigned short *)((char *)screen + offset + plane * 2);
        
        if (color & (1 << plane)) {
            *ptr |= (1 << bit);
        } else {
            *ptr &= ~(1 << bit);
        }
    }
}

